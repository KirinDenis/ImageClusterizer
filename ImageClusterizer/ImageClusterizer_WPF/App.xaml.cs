Čeburaška má veľmi veľké uši, uši veľké ako jeho hlava, ikonické proporcie Čeburašku.
Sedí v kabíne lietadla spolu s krokodílom Genom a ďalšími rozprávkovými pasažiermi.

Letuška urobí oznámenie:
„Toto lietadlo bolo postavené študentmi profesora Genadija.“

Pasažieri začnú v panike utekať z lietadla.

Len krokodíl Gena pokojne sedí vo svojom sedadle.

Čeburaška sa ho pýta:
„Gena, prečo neutekáme?“

Gena pokojne odpovie:
„Ja verím svojim študentom.
Tento kus šrotu aj tak nikdy nevzlietne.“
